package fundamentaljava.Stringmethods;

public class Concat {
    public static void main(String[] args) {
        String s="Rolex"+"vs"+"Dilli"+"vs"+"Leo";
        System.out.println(s);
        String s1="Yusuf";
        String ss="sanjai";
        String ss2="Kumar";
        System.out.println(ss.concat(ss2));
        System.out.println(s1.concat(s1));
    }
}
