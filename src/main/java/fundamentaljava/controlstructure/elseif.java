package fundamentaljava.controlstructure;

public class elseif {
    public static void main(String[] args) {
        String name="yusuf";
        if (name=="abdul")
        {
            System.out.println("name is abdul");
        }
        else if(name=="raj")
        {
            System.out.println("name is raj");
        }
        else if (name=="abdul")
        {
            System.out.println("name is abdul");
        }
        else if (name=="ram")
        {
            System.out.println("name is ram");
        }
        else if (name=="yusuf")
        {
            System.out.println("name is yusuf");
        }
        else
        {
            System.out.println("invalid");
        }
    }
}
