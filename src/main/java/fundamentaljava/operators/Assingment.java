package fundamentaljava.operators;

public class Assingment {
    public static void main(String[] args) {
        int value=10;
        int value1=5;
        int value2=10;
        value+=1;
        value1+=2;
        value2+=1000;
        System.out.println(value);
        System.out.println(value1);
        System.out.println(value2);
    }
}
